package day08;

public class C06_Substring {
    public static void main(String[] args) {
        String str = "Orhan java ogreniyor";
        System.out.println(str.substring(0,1));

        String str2 = "Orhan java ögrenmeye calisiyor,yardim edin";
        System.out.println(str2.substring(0,5));

        String str3 = "Orhan java ögrenmeye calisiyor,yardim edin";
        System.out.println(str3.substring(0,17));

        //Substring : bize verilen degerlerde istedigimiz yere kadar ki degerlendirmeyi görmemizi saglar

    }
}
